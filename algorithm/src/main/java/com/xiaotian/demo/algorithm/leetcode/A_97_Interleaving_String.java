package com.xiaotian.demo.algorithm.leetcode;

public class A_97_Interleaving_String {

    public boolean isInterleave(String s1, String s2, String s3) {
        if (s1 == null && s2 == null && s3 == null) {
            return true;
        }
        if (s3 == null) {
            return false;
        }


        return true;
    }
}
